namespace Xiyu.VirtualLiveRoom.Component.Character.Emotion
{
    /// <summary>
    /// 枚举表示角色的基本情绪。
    /// </summary>
    public enum CharacterBasicEmotions
    {
        /// <summary>
        /// 快乐：当需求得到满足，或经历积极事件时的愉悦感受。
        /// </summary>
        Happiness,

        /// <summary>
        /// 悲伤：失去亲人、失败或遭受挫折时的哀伤情绪。
        /// </summary>
        Sadness,

        /// <summary>
        /// 愤怒：当感受到威胁、不公或受阻时产生的强烈不满和敌意。
        /// </summary>
        Anger,

        /// <summary>
        /// 恐惧：面对危险、未知或威胁性情境时的害怕和紧张。
        /// </summary>
        Fear,

        /// <summary>
        /// 惊讶：对意外、不寻常或突然发生的事件产生的短暂反应。
        /// </summary>
        Surprise,

        /// <summary>
        /// 厌恶：对令人不悦、恶心或道德上不被接受的事物产生的反感。
        /// </summary>
        Disgust,

        /// <summary>
        /// 内疚：因做错事或违背道德标准而产生的自责和懊悔。
        /// </summary>
        Guilt,

        /// <summary>
        /// 兴奋：对即将发生的事情充满期待和热情的高涨情绪。
        /// </summary>
        Excitement,

        /// <summary>
        /// 嫉妒：因他人拥有自己渴望的东西而产生的羡慕和不满。
        /// </summary>
        Jealousy,

        /// <summary>
        /// 爱（此处应为Love，可能是Ove的拼写错误）：对某人或某物产生的深厚情感和关怀。
        /// </summary>
        Love,

        /// <summary>
        /// 羞耻：因个人行为、外貌或身份而感到尴尬和自卑。
        /// </summary>
        Shame,

        /// <summary>
        /// 尴尬：在社交场合中因不当行为或意外情况而产生的不自在感。
        /// </summary>
        Embarrassment,

        /// <summary>
        /// 满足：需求得到满足后的满足和幸福感。
        /// </summary>
        Satisfaction,

        /// <summary>
        /// 挫败：努力未能达到预期目标时的失落和沮丧。
        /// </summary>
        Frustration,

        /// <summary>
        /// 期待：对即将发生的好事充满期待和憧憬。
        /// </summary>
        Anticipation,

        /// <summary>
        /// 钦佩：对某人或某事的才能、成就或品质产生的尊敬和赞赏。
        /// </summary>
        Admiration,

        /// <summary>
        /// 崇拜：对某人或某事物极度尊敬和敬仰，甚至可能达到迷信的程度。
        /// </summary>
        Worship,

        /// <summary>
        /// 欣赏：对艺术、自然、他人等优点或美感的认可和喜爱。
        /// </summary>
        Appreciation,

        /// <summary>
        /// 敬畏：对神圣、庄严或强大力量产生的尊敬和畏惧混合的情感。
        /// </summary>
        Awe,

        /// <summary>
        /// 厌倦：对重复、单调或无趣的事物产生的不耐烦和反感。
        /// </summary>
        Boredom,

        /// <summary>
        /// 困惑：对复杂、不明确或难以理解的事物产生的疑惑和迷茫。
        /// </summary>
        Confusion,

        /// <summary>
        /// 渴望：对某人、某物或某种状态产生的强烈向往和追求。
        /// </summary>
        Desire,

        /// <summary>
        /// 着迷：对某人、某物或某种活动产生的极度兴趣和沉迷。
        /// </summary>
        Fascination,

        /// <summary>
        /// 怀旧：对过去美好时光的回忆和怀念，可能伴随着温馨、忧伤或遗憾。
        /// </summary>
        Nostalgia,

        /// <summary>
        /// 浪漫：对爱情、美好事物或理想化情境的向往和追求。
        /// </summary>
        Romance,

        /// <summary>
        /// 性欲：与性吸引和性需求相关的情感和欲望。
        /// </summary>
        SexualDesire,

        /// <summary>
        /// 同情：对他人不幸或困境产生的理解和怜悯。
        /// </summary>
        Sympathy,

        /// <summary>
        /// 鄙视：对某人或某事的品质、行为或观点产生的不屑和轻蔑。
        /// </summary>
        Disdain,

        /// <summary>
        /// 轻蔑：对某人或某事的轻视和贬低，通常伴随着不屑一顾的态度。
        /// </summary>
        Contempt,

        /// <summary>
        /// 焦虑：对未来可能发生的负面事件产生的担忧、紧张和不安。
        /// </summary>
        Anxiety,

        /// <summary>
        /// 抑郁：长期情绪低落、兴趣丧失和精力减退的状态，更偏向于指临床上的抑郁症或长期的情绪低落。
        /// </summary>
        Depression,

        /// <summary>
        /// 感激：对他人给予的帮助、支持或恩惠产生的感谢和珍惜。
        /// </summary>
        Gratitude,

        /// <summary>
        /// 骄傲：对自己的成就、品质或所属团体产生的自豪和自信。
        /// </summary>
        Pride,

        /// <summary>
        /// 平静：内心平和、没有强烈情绪波动的状态，也表示内心的平和与安宁（或Peace）。
        /// </summary>
        Calmness
    }
}